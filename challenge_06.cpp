//6) Sister Crow wanted to build a nest of loons.
//Each side of her 4-sided nest needs 50 loons.
//How many loons will she need?



#include<iostream>
using namespace std;

int main(){
    int sides = 4;
    int loons = 50;
    int total;

    cout << "6) Sister Crow wanted to build a nest of loons. Each side of her 4-sided nest needs 50 loons. How many loons will she need?" << endl;
    total = sides * loons;
    cout << "Total Loons Needed : " << total << endl ;

    return 0;
}
