//1) Once upon a time, there were three little crows - ages 2, 4, and 6. What is the total of their ages?
#include <iostream>
using namespace std;

int main() {
    // Define the ages of the crows
    int age1 = 2;
    int age2 = 4;
    int age3 = 6;

    // Calculate the total age
    int totalAge = age1 + age2 + age3;

    // Print the total age
    std::cout << "1) Once upon a time, there were three little crows - ages 2, 4, and 6. What is the total of their ages?" << endl;
    std::cout << "The total age of the crows is: " << totalAge << std::endl;

    return 0;
}

